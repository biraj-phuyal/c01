#include <stdio.h>

void	ft_rev_int_tab(int *tab, int size)
{
	while (tab > size)
	{
		tab++;
	}
}

int main()
{
	int A[] = {1,2,3,4,5};
	int a = sizeof(A);
	ft_rev_int_tab(A, a);
}
